namespace PokerAlgo;
/// <summary>
/// Represents the four suits in a standard deck of playing cards.
/// </summary>
public enum CardSuit
{
    /// <summary>
    /// The Spades suit (♠).
    /// </summary>
    Spades,

    /// <summary>
    /// The Clubs suit (♣).
    /// </summary>
    Clubs,

    /// <summary>
    /// The Hearts suit (♥).
    /// </summary>
    Hearts,

    /// <summary>
    /// The Diamonds suit (♦).
    /// </summary>
    Diamonds
}